class Aspirante:

    # Constructor de la clase aspirante
    def __init__(self, id, nombre, telefono, email, mascotas, familia, detalles, desempeno, educacion, sitios_estudio
                 , lugar_trabajo, nombre_empresa, ingreso_mensual, modalidad_trabajo, liderado, personas_cargo, roles,
                 anos_exp, info_pro):
        self.id = id
        self.nombre = nombre
        self.telefono = telefono
        self.email = email
        self.mascotas = mascotas
        self.familia = familia
        self.detalles = detalles
        self.desempeno = desempeno
        self.educacion = educacion
        self.sitios_estudio = sitios_estudio
        self.lugar_trabajo = lugar_trabajo
        self.nombre_empresa = nombre_empresa
        self.ingreso_mensual = ingreso_mensual
        self.modalidad_trabajo = modalidad_trabajo
        self.liderado = liderado
        self.personas_a_cargo = personas_cargo
        self.roles = roles
        self.exp = anos_exp
        self.info = info_pro
